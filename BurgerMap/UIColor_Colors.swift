import UIKit
import UIColor_Hex_Swift

extension UIColor {
    class func burgerOrange() -> UIColor {
        // #F5A623
        return UIColor(rgba: "#F4A623")
    }
    
    class func burgerDarkGray() -> UIColor {
        // #252424
        return UIColor(rgba: "#252424")
    }
}